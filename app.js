// Запитуємо у користувача кількість годин
let hours = prompt("Введіть кількість годин");

// Перевіряємо, чи ввів користувач число
if (!isNaN(hours)) {
  // Пораховуємо кількість секунд у введеній кількості годин
  let seconds = hours * 60 * 60;

  // Виводимо результат користувачу
  alert(`У ${hours} годинах ${seconds} секунд`);
} else {
  alert("Будь ласка, введіть валідне число");
}